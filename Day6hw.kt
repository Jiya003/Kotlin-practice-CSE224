fun main() {
    val a = true
    val b = false

    // Example of logical AND
    val result1 = a && b // result1 will be false
    println("a && b = $result1") // Output: a && b = false
    
     val result2 = a || b // result2 will be true
    println("a || b = $result2") // Output: a || b = true
    
    
}
